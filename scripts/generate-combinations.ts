// Validate
let errors = 0;
const qualSets: string[][] = [];
let optIdx = 0;
for (const nonQual of combinations(ALL_GROUPS, 4)) {
  const qualSet = new Set(ALL_GROUPS);
  nonQual.forEach(g => qualSet.delete(g));
  const qualifying = [...qualSet].sort();
  qualSets.push(qualifying);

  const row = WIKI_DATA[optIdx];
  if (!row) {
    console.error(`Missing row ${optIdx + 1}`);
    errors++;
    optIdx++;
    continue;
  }

  // Check all 8 values are unique
  const valSet = new Set(row);
  if (valSet.size !== 8) {
    console.error(`Option ${optIdx + 1}: Duplicate values: ${row.join(',')}`);
    errors++;
  }

  // Check all values are from qualifying groups
  for (const val of row) {
    if (!qualSet.has(val)) {
      console.error(`Option ${optIdx + 1}: Value ${val} not in qualifying groups ${qualifying.join(',')}`);
      errors++;
    }
  }

  // Check each value is in the allowed set for its position
  for (let i = 0; i < POSITIONS.length; i++) {
    const pos = POSITIONS[i];
    const val = row[i];
    if (!ALLOWED_GROUPS[pos].includes(val)) {
      console.error(`Option ${optIdx + 1}: Value ${val} not allowed in position ${pos} (allowed: ${ALLOWED_GROUPS[pos].join(',')})`);
      errors++;
    }
  }

  optIdx++;
}

console.log(`\nTotal rows: ${WIKI_DATA.length}`);
console.log(`Validation errors: ${errors}`);

if (errors === 0) {
  // Generate the JSON output
  const output = WIKI_DATA.map((row, i) => ({
    option: i + 1,
    '1A': row[0], '1B': row[1], '1D': row[2], '1E': row[3],
    '1G': row[4], '1I': row[5], '1K': row[6], '1L': row[7],
  }));

  const fs = require('fs');
  const path = require('path');
  const outPath = path.join(__dirname, '..', 'data', 'seed', 'knockout-third-place-combinations.json');
  fs.writeFileSync(outPath, JSON.stringify(output, null, 2));
  console.log(`\nWrote ${output.length} combinations to ${outPath}`);
}
